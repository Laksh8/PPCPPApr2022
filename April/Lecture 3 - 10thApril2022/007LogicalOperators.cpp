#include<iostream>

using namespace std;

int main() {

	cout << (true && true) << endl;
	cout << (true && false) << endl;
	cout << (false and true) << endl;
	cout << (false and false) << endl;

	cout << endl;

	cout << (true || true) << endl;
	cout << (true || false) << endl;
	cout << (false or true) << endl;
	cout << (false or false) << endl;

	cout << endl;

	cout << !true << endl;
	cout << !false << endl;

	cout << endl;

	cout << (-3 && 7) << endl;
	cout << (0 && 10) << endl;


	return 0;
}